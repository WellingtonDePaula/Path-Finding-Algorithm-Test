package wellz.domain;

public class FinalPos {
    private final int row = 4;
    private final int col = 4;
    private final String pointer = "x";

    public int getRow() {
        return this.row;
    }

    public int getCol() {
        return this.col;
    }

    public String getPointer() {
        return this.pointer;
    }
}
